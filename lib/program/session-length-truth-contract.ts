// LOCAL READ-ONLY SHAPES (defensive — every field optional)
// =============================================================================

interface VariantLike {
  duration?: number | null
  label?: string | null
  compressionLevel?: 'none' | 'light' | 'moderate' | 'heavy' | null
  selection?: {
    main?: VariantRowLike[] | null
    warmup?: VariantRowLike[] | null
    cooldown?: VariantRowLike[] | null
  } | null
}

interface VariantRowLike {
  exercise?: {
    id?: string | null
    name?: string | null
    category?: string | null
  } | null
  sets?: number | null
  repsOrTime?: string | number | null
  setSeconds?: number | null
  restSeconds?: number | null
  selectionReason?: string | null
}

interface SessionLike {
  dayNumber?: number | null
  estimatedMinutes?: number | null
  variants?: VariantLike[] | null
  exercises?: Array<{
    id?: string | null
    name?: string | null
    category?: string | null
    sets?: number | null
    setSeconds?: number | null
    restSeconds?: number | null
    selectionReason?: string | null
  }> | null
  sessionLengthTruth?: SessionLengthTruthStamp
}

interface ProgramLike {
  generatedAt?: string | null
  sessions?: SessionLike[] | null
  sessionLengthTruth?: ProgramSessionLengthTruthStamp
}

// =============================================================================
// HELPERS — pure, deterministic, no side effects
// =============================================================================

function asNumber(n: unknown, fallback: number): number {
  return typeof n === 'number' && Number.isFinite(n) ? n : fallback
}

function asString(s: unknown, fallback: string): string {
  return typeof s === 'string' && s.length > 0 ? s : fallback
}

function rowsOf(variant: VariantLike | undefined): VariantRowLike[] {
  return Array.isArray(variant?.selection?.main) ? variant!.selection!.main! : []
}

function rowExerciseId(row: VariantRowLike | null | undefined): string {
  return asString(row?.exercise?.id, '')
}

function rowExerciseName(row: VariantRowLike | null | undefined): string {
  return asString(row?.exercise?.name, '')
}

function rowSets(row: VariantRowLike | null | undefined): number {
  return asNumber(row?.sets, 0)
}

/**
 * Estimate one row's total time in seconds. Honest — uses explicit fields
 * where present, sane defaults where absent. Never returns negative.
 */
function rowEstimatedSeconds(row: VariantRowLike | null | undefined): number {
  if (!row) return 0
  const sets = Math.max(0, asNumber(row.sets, 0))
  if (sets <= 0) return 0
  const setSecs = Math.max(0, asNumber(row.setSeconds, DEFAULT_SET_SECONDS))
  const restSecs = Math.max(0, asNumber(row.restSeconds, DEFAULT_REST_SECONDS))
  // Rest is between sets, so (sets - 1) intervals.
  const restCount = Math.max(0, sets - 1)
  return sets * setSecs + restCount * restSecs
}

/** Sum estimated minutes for all main rows. Rounded to nearest minute. */
function variantEstimatedMinutes(variant: VariantLike | undefined): number {
  const rows = rowsOf(variant)
  let totalSecs = 0
  for (const r of rows) totalSecs += rowEstimatedSeconds(r)
  return Math.max(0, Math.round(totalSecs / 60))
}

function totalSetsOf(variant: VariantLike | undefined): number {
  let total = 0
  for (const r of rowsOf(variant)) total += rowSets(r)
  return total
}

function orderedIdentitySignature(variant: VariantLike | undefined): string {
  return rowsOf(variant)
    .map((r) => rowExerciseId(r))
    .join('|')
}

/** Maps `variant.duration` to a 3-bucket execution mode tag. */
function modeFor(durationMinutes: number): 'full' | '45_min' | '30_min' {
  if (durationMinutes <= 35) return '30_min'
  if (durationMinutes <= 50) return '45_min'
  return 'full'
}

/** Did this variant preserve at least one primary-skill row? */
function detectPrimarySkillAnchor(variant: VariantLike | undefined): boolean {
  for (const r of rowsOf(variant)) {
    const cat = asString(r?.exercise?.category, '').toLowerCase()
    const reason = asString(r?.selectionReason, '').toLowerCase()
    if (cat === 'skill') return true
    if (reason.includes('primary') && reason.includes('skill')) return true
    if (reason.includes('skill progression')) return true
  }
  return false
}

function detectSecondaryOrHybridSkill(variant: VariantLike | undefined): boolean {
  for (const r of rowsOf(variant)) {
    const reason = asString(r?.selectionReason, '').toLowerCase()
    if (reason.includes('secondary') && reason.includes('skill')) return true
    if (reason.includes('hybrid')) return true
    if (reason.includes('expression')) return true
  }
  return false
}

function detectEssentialStrengthSupport(variant: VariantLike | undefined): boolean {
  for (const r of rowsOf(variant)) {
    const cat = asString(r?.exercise?.category, '').toLowerCase()
    const reason = asString(r?.selectionReason, '').toLowerCase()
    if (cat === 'strength' && (reason.includes('support') || reason.includes('strength'))) {
      return true
    }
  }
  return false
}

/** Names present in Full but absent from `variant`, by id (fallback to name). */
function deferredNamesVsFull(
  variant: VariantLike | undefined,
  fullVariant: VariantLike | undefined,
): string[] {
  const present = new Set<string>()
  for (const r of rowsOf(variant)) {
    const id = rowExerciseId(r)
    if (id) present.add(`id:${id}`)
    else {
      const name = rowExerciseName(r)
      if (name) present.add(`name:${name}`)
    }
  }
  const deferred: string[] = []
  for (const r of rowsOf(fullVariant)) {
    const id = rowExerciseId(r)
    const name = rowExerciseName(r)
    const key = id ? `id:${id}` : name ? `name:${name}` : ''
    if (!key) continue
    if (!present.has(key)) deferred.push(name || id)
  }
  return deferred
}

// =============================================================================
// CORE — build per-variant truth receipt
// =============================================================================

function buildVariantTruth(
  variant: VariantLike,
  variantIndex: number,
  fullVariant: VariantLike | undefined,
): SessionLengthVariantTruth {
  const targetMinutes = Math.max(0, asNumber(variant.duration, 0))
  const actualEstimatedMinutes = variantEstimatedMinutes(variant)
  const labelDriftMinutes = Math.abs(targetMinutes - actualEstimatedMinutes)
  const labelDriftWarning =
    targetMinutes > 0 && labelDriftMinutes > LABEL_DRIFT_TOLERANCE_MIN
  const compressionLevel =
    (variant.compressionLevel as SessionLengthVariantTruth['compressionLevel']) ?? 'none'
  const totalMainSets = totalSetsOf(variant)
  const mainExerciseCount = rowsOf(variant).length
  const deferred = fullVariant && variantIndex !== 0 ? deferredNamesVsFull(variant, fullVariant) : []

  const fullMainCount = fullVariant ? rowsOf(fullVariant).length : mainExerciseCount
  const fullTotalSets = fullVariant ? totalSetsOf(fullVariant) : totalMainSets
  const fullDuration = fullVariant ? asNumber(fullVariant.duration, targetMinutes) : targetMinutes
  const sameMainCount = mainExerciseCount === fullMainCount
  const sameOrderedIdentity =
    fullVariant != null && orderedIdentitySignature(variant) === orderedIdentitySignature(fullVariant)
  const sameTotalSets = totalMainSets === fullTotalSets
  const nearEqualDuration = Math.abs(targetMinutes - fullDuration) < DURATION_DISTINCTNESS_THRESHOLD_MIN
  const materiallyDistinct =
    variantIndex === 0 ? false : !(sameMainCount && sameOrderedIdentity && sameTotalSets && nearEqualDuration)

  // Liveness mirrors `isVariantLaunchable` — every row needs id + name.
  let liveWorkoutLaunchable = mainExerciseCount > 0
  if (liveWorkoutLaunchable) {
    for (const r of rowsOf(variant)) {
      if (!rowExerciseId(r) || !rowExerciseName(r)) {
        liveWorkoutLaunchable = false
        break
      }
    }
  }

  return {
    variantIndex,
    label: asString(variant.label, variantIndex === 0 ? 'Full Session' : `Variant ${variantIndex}`),
    mode: modeFor(targetMinutes),
    targetMinutes,
    actualEstimatedMinutes,
    labelDriftMinutes,
    labelDriftWarning,
    compressionLevel,
    totalMainSets,
    mainExerciseCount,
    deferredExerciseNames: deferred,
    priorityPreservation: {
      primarySkillAnchorPreserved: detectPrimarySkillAnchor(variant),
      secondaryOrHybridSkillPreserved: detectSecondaryOrHybridSkill(variant),
      essentialStrengthSupportPreserved: detectEssentialStrengthSupport(variant),
    },
    distinctVsFull: {
      sameMainCount,
      sameOrderedIdentity,
      sameTotalSets,
      nearEqualDuration,
      materiallyDistinct,
    },
    liveWorkoutLaunchable,
  }
}

// =============================================================================
// CORE — build session-level Phase R stamp
// =============================================================================

function buildSessionStamp(session: SessionLike): SessionLengthTruthStamp {
  const dayNumber = asNumber(session.dayNumber, 0)
  const variants = Array.isArray(session.variants) ? session.variants : null

  if (!variants || variants.length === 0) {
    return {
      version: 'phase-r-session-length-truth-v1',
      dayNumber,
      verdict: 'LEGACY_NO_VARIANTS',
      summary: 'Session length: legacy session — no variant trio available.',
      variantTruth: [],
      rollup: {
        launchableVariantCount: 0,
        distinctShortCount: 0,
        deferredExerciseTotal: 0,
        setDeltaTotal: 0,
        primarySkillAnchorPreservedAcrossShorts: false,
      },
      liveWorkoutHandoff: {
        mechanism: 'launch_fingerprint',
        payloadCompatibility: 'NO_VARIANTS',
        launchableVariantCount: 0,
      },
    }
  }

  const fullVariant = variants[0]
  const variantTruth: SessionLengthVariantTruth[] = variants.map((v, idx) =>
    buildVariantTruth(v, idx, fullVariant),
  )

  const launchableVariantCount = variantTruth.filter((v) => v.liveWorkoutLaunchable).length
  const distinctShorts = variantTruth.filter(
    (v) => v.variantIndex > 0 && v.distinctVsFull.materiallyDistinct && v.liveWorkoutLaunchable,
  )
  const distinctShortCount = distinctShorts.length

  // Deferred + set deltas are the structural-truth signals.
  const fullSets = variantTruth[0]?.totalMainSets ?? 0
  let deferredExerciseTotal = 0
  let setDeltaTotal = 0
  let primarySkillAnchorPreservedAcrossShorts = distinctShorts.length > 0
  for (const s of distinctShorts) {
    deferredExerciseTotal += s.deferredExerciseNames.length
    setDeltaTotal += Math.max(0, fullSets - s.totalMainSets)
    if (!s.priorityPreservation.primarySkillAnchorPreserved) {
      primarySkillAnchorPreservedAcrossShorts = false
    }
  }

  // Verdict logic — honest, conservative.
  let verdict: SessionLengthTruthStamp['verdict']
  if (variants.length <= 1) {
    verdict = 'NO_LAUNCHABLE_SHORTS'
  } else if (distinctShortCount === 0) {
    verdict = 'NO_LAUNCHABLE_SHORTS'
  } else if (deferredExerciseTotal === 0 && setDeltaTotal === 0) {
    // Shorts exist and are distinct, but only on duration — no exercise drop,
    // no set drop. That is a label-parity short. Honest yellow flag.
    verdict = 'SHORTS_AT_LABEL_PARITY'
  } else {
    verdict = 'STRUCTURALLY_REAL'
  }

  // Payload compatibility maps cleanly from verdict.
  const payloadCompatibility: SessionLengthLiveWorkoutHandoff['payloadCompatibility'] =
    verdict === 'STRUCTURALLY_REAL' || verdict === 'SHORTS_AT_LABEL_PARITY'
      ? 'OK'
      : verdict === 'NO_LAUNCHABLE_SHORTS' && variants.length > 1
        ? 'COSMETIC_ONLY'
        : 'NO_VARIANTS'

  // Summary copy.
  const summary = (() => {
    if (verdict === 'LEGACY_NO_VARIANTS') return 'Session length: legacy session — no variants.'
    if (verdict === 'NO_LAUNCHABLE_SHORTS') {
      return 'Session length: full only — short modes not applicable for this day.'
    }
    if (verdict === 'SHORTS_AT_LABEL_PARITY') {
      return `Session length: ${distinctShortCount} short mode${distinctShortCount === 1 ? '' : 's'} present, label-only differences.`
    }
    // STRUCTURALLY_REAL
    const parts: string[] = []
    parts.push(
      `${distinctShortCount + 1} mode${distinctShortCount === 0 ? '' : 's'} structurally distinct`,
    )
    if (deferredExerciseTotal > 0) {
      parts.push(
        `short${distinctShortCount === 1 ? '' : 's'} drop ${deferredExerciseTotal} accessor${deferredExerciseTotal === 1 ? 'y' : 'ies'}`,
      )
    }
    if (primarySkillAnchorPreservedAcrossShorts) parts.push('primary skill anchor preserved')
    return `Session length: ${parts.join(' · ')}.`
  })()

  return {
    version: 'phase-r-session-length-truth-v1',
    dayNumber,
    verdict,
    summary,
    variantTruth,
    rollup: {
      launchableVariantCount,
      distinctShortCount,
      deferredExerciseTotal,
      setDeltaTotal,
      primarySkillAnchorPreservedAcrossShorts,
    },
    liveWorkoutHandoff: {
      mechanism: 'launch_fingerprint',
      payloadCompatibility,
      launchableVariantCount,
    },
  }
}

// =============================================================================
// CORE — program rollup
// =============================================================================

function buildProgramStamp(
  perSessionStamps: SessionLengthTruthStamp[],
): ProgramSessionLengthTruthStamp {
  const sessionCounts = {
    total: perSessionStamps.length,
    structurallyReal: 0,
    labelParity: 0,
    noLaunchableShorts: 0,
    legacyNoVariants: 0,
  }
  for (const s of perSessionStamps) {
    if (s.verdict === 'STRUCTURALLY_REAL') sessionCounts.structurallyReal++
    else if (s.verdict === 'SHORTS_AT_LABEL_PARITY') sessionCounts.labelParity++
    else if (s.verdict === 'NO_LAUNCHABLE_SHORTS') sessionCounts.noLaunchableShorts++
    else if (s.verdict === 'LEGACY_NO_VARIANTS') sessionCounts.legacyNoVariants++
  }

  let programVerdict: ProgramSessionLengthTruthStamp['programVerdict']
  if (sessionCounts.total === 0) programVerdict = 'NO_VARIANTS_AVAILABLE'
  else if (sessionCounts.legacyNoVariants === sessionCounts.total) programVerdict = 'NO_VARIANTS_AVAILABLE'
  else if (
    sessionCounts.structurallyReal > 0 &&
    sessionCounts.structurallyReal + sessionCounts.noLaunchableShorts === sessionCounts.total
  ) {
    // Every session either structurally compresses or doesn't need shorts.
    programVerdict =
      sessionCounts.structurallyReal === sessionCounts.total - sessionCounts.noLaunchableShorts
        ? 'STRUCTURALLY_REAL_ACROSS_PROGRAM'
        : 'STRUCTURALLY_REAL_PARTIAL'
  } else if (sessionCounts.structurallyReal > 0) {
    programVerdict = 'STRUCTURALLY_REAL_PARTIAL'
  } else if (sessionCounts.labelParity > 0) {
    programVerdict = 'SHORTS_PRESENT_BUT_LABEL_PARITY'
  } else {
    programVerdict = 'NO_SHORTS_NEEDED'
  }

  const summary =
    programVerdict === 'STRUCTURALLY_REAL_ACROSS_PROGRAM'
      ? `Session length: structurally real on every applicable day (${sessionCounts.structurallyReal}/${sessionCounts.total}).`
      : programVerdict === 'STRUCTURALLY_REAL_PARTIAL'
        ? `Session length: structurally real on ${sessionCounts.structurallyReal}/${sessionCounts.total} day${sessionCounts.total === 1 ? '' : 's'}.`
        : programVerdict === 'SHORTS_PRESENT_BUT_LABEL_PARITY'
          ? `Session length: shorts exist but only differ by label on ${sessionCounts.labelParity}/${sessionCounts.total} day${sessionCounts.total === 1 ? '' : 's'}.`
          : programVerdict === 'NO_SHORTS_NEEDED'
            ? 'Session length: full sessions only — short modes not applicable.'
            : 'Session length: legacy program — no variant trio available.'

  return {
    version: 'phase-r-session-length-truth-v1',
    generatedAt: new Date().toISOString(),
    programVerdict,
    sessionCounts,
    summary,
  }
}

