/**
 * Per-mode truth receipt. One entry per launchable variant in `variants[]`.
 * Order mirrors `variants[]`: index 0 is Full, idx 1+ are shorts.
 */
export interface SessionLengthVariantTruth {
  /** Index in the underlying `session.variants[]` array. */
  variantIndex: number
  /** Human label as authored by `generateSessionVariants` (`'Full Session'`,
   * `'45 Min'`, `'30 Min'`). */
  label: string
  /**
   * Mode tag derived from `variant.duration`. Matches the card's
   * `selectedExecutionMode`: 'full' (>50m), '45_min' (35–50m), '30_min' (≤35m).
   */
  mode: 'full' | '45_min' | '30_min'
  /** Variant.duration as authored upstream — the user-facing target. */
  targetMinutes: number
  /**
   * ACTUAL estimated minutes computed deterministically from the variant
   * body (sum over rows of `setSeconds × sets + restSeconds × max(sets-1, 0)`,
   * with safe per-row fallbacks). NOT pulled from a label. NOT static.
   */
  actualEstimatedMinutes: number
  /** Drift between target and actual, in minutes (always ≥ 0). */
  labelDriftMinutes: number
  /**
   * True when `labelDriftMinutes > LABEL_DRIFT_TOLERANCE_MIN`. The card uses
   * this to render an honest "estimated 36 min because…" message instead of
   * pretending the target lands exactly.
   */
  labelDriftWarning: boolean
  /** Compression level the engine reported for this variant. */
  compressionLevel: 'none' | 'light' | 'moderate' | 'heavy'
  /**
   * Total sets in this variant's main block. Used as the structural-distinctness
   * signal alongside main count and ordered identity.
   */
  totalMainSets: number
  /** Number of main exercises in this variant. */
  mainExerciseCount: number
  /**
   * Names of exercises present in canonical Full but ABSENT in this variant
   * (deferred). Pulled by id-difference; safe when ids missing.
   */
  deferredExerciseNames: string[]
  /**
   * Per-row reasons captured from `selectionReason` on rows that survived,
   * grouped by priority bucket. We do NOT invent reasons — only surface what
   * was already labelled by the engine.
   */
  priorityPreservation: {
    /**
     * True when at least one row in this variant has a primary-skill marker
     * (category === 'skill', or selectionReason mentions "primary"/"skill
     * progression"). The compressor's `preserveSkillWork: true` flag is
     * what makes this likely; we verify it actually held.
     */
    primarySkillAnchorPreserved: boolean
    /** Same check for secondary skill / hybrid expressions. */
    secondaryOrHybridSkillPreserved: boolean
    /**
     * True when an essential strength-support row survived in this variant
     * (selectionReason includes 'support'/'strength' for category 'strength').
     */
    essentialStrengthSupportPreserved: boolean
  }
  /** Material-distinctness check vs the canonical Full variant.
   * (Cosmetic shorts should already have been dropped upstream — this is
   *  belt-and-suspenders so the trace doesn't lie.) */
  distinctVsFull: {
    sameMainCount: boolean
    sameOrderedIdentity: boolean
    sameTotalSets: boolean
    nearEqualDuration: boolean
    materiallyDistinct: boolean
  }
  /**
   * `true` iff every row carries a usable exercise identity (id + name) —
   * mirrors `isVariantLaunchable`'s minimum invariants from the engine.
   */
  liveWorkoutLaunchable: boolean
}

/**
 * Live-workout handoff audit. Phase R does not change the handoff corridor —
 * it documents how the variant the card displays reaches the live workout so
 * the master-truth blueprint can see the path is unbroken.
 */
export interface SessionLengthLiveWorkoutHandoff {
  /**
   * Always 'launch_fingerprint' in the current architecture. The card stamps
   * via `stampLaunchFingerprint` and the route reads via
   * `readLaunchFingerprint`. This field exists so a future change is auditable.
   */
  mechanism: 'launch_fingerprint'
  /**
   * `'OK'` when the selected variant index can produce a real launchable
   * body and the URL params (`mode`, `variant`, `day`, `week`) round-trip
   * cleanly.
   * `'NO_VARIANTS'` when no shorts exist (only Full).
   * `'COSMETIC_ONLY'` when all shorts failed material-distinctness.
   * `'PARITY_DRIFT'` is reserved for the runtime-side fingerprint compare;
   * we do not stamp it here, the route owns it.
   */
  payloadCompatibility: 'OK' | 'NO_VARIANTS' | 'COSMETIC_ONLY'
  /** Number of launchable distinct variants in `session.variants[]`. */
  launchableVariantCount: number
}

/**
 * Per-session Phase R stamp. Stored at `session.sessionLengthTruth`.
 * Optional, JSON-safe, round-trips through every existing `...session` spread.
 */
export interface SessionLengthTruthStamp {
  version: 'phase-r-session-length-truth-v1'
  dayNumber: number
  /** Honest verdict for the WHOLE session.
   *  - STRUCTURALLY_REAL: ≥1 launchable distinct short variant exists AND the
   *    short(s) drop exercises and/or sets vs Full.
   *  - SHORTS_AT_LABEL_PARITY: shorts exist and are distinct, but they only
   *    differ on duration label (no exercise drop, no set drop). Honest
   *    yellow flag.
   *  - NO_LAUNCHABLE_SHORTS: only Full remains (engine rejected cosmetic
   *    shorts upstream, OR full session is already ≤ 35m and engine skipped
   *    the 45/30 generation). Not a failure — short modes are not applicable.
   *  - LEGACY_NO_VARIANTS: session has no `variants[]` array (older program
   *    objects).
   */
  verdict:
    | 'STRUCTURALLY_REAL'
    | 'SHORTS_AT_LABEL_PARITY'
    | 'NO_LAUNCHABLE_SHORTS'
    | 'LEGACY_NO_VARIANTS'
  /** One-line summary the card renders. Always concise. */
  summary: string
  /** Full + (optional) 45 + (optional) 30. Order matches `session.variants[]`. */
  variantTruth: SessionLengthVariantTruth[]
  /** Aggregate counts for the program-level rollup. */
  rollup: {
    launchableVariantCount: number
    distinctShortCount: number
    deferredExerciseTotal: number
    setDeltaTotal: number
    primarySkillAnchorPreservedAcrossShorts: boolean
  }
  liveWorkoutHandoff: SessionLengthLiveWorkoutHandoff
}

/**
 * Program-level Phase R rollup. Stamped at `program.sessionLengthTruth`.
 */
export interface ProgramSessionLengthTruthStamp {
  version: 'phase-r-session-length-truth-v1'
  generatedAt: string
  /** Worst-state verdict across all sessions. */
  programVerdict:
    | 'STRUCTURALLY_REAL_ACROSS_PROGRAM'
    | 'STRUCTURALLY_REAL_PARTIAL'
    | 'SHORTS_PRESENT_BUT_LABEL_PARITY'
    | 'NO_SHORTS_NEEDED'
    | 'NO_VARIANTS_AVAILABLE'
  /** Per-session counts for the master-truth blueprint. */
  sessionCounts: {
    total: number
    structurallyReal: number
    labelParity: number
    noLaunchableShorts: number
    legacyNoVariants: number
  }
  /** One-line summary for the program page. */
  summary: string
}

