/**
 * Infer difficulty level from exercise characteristics
 */
export function inferDifficultyLevel(exercise: Exercise): DifficultyLevel {
  if (exercise.difficultyLevel) return exercise.difficultyLevel
  
  const demand = exercise.neuralDemand + exercise.fatigueCost
  if (demand <= 4) return 'beginner'
  if (demand <= 6) return 'intermediate'
  if (demand <= 8) return 'advanced'
  return 'elite'
}

/**
 * Infer movement category from movement pattern
 */
export function inferMovementCategory(exercise: Exercise): MovementCategory {
  if (exercise.movementCategory) return exercise.movementCategory
  
  const pattern = exercise.movementPattern
  if (pattern === 'horizontal_push' || pattern === 'vertical_push') return 'push'
  if (pattern === 'horizontal_pull' || pattern === 'vertical_pull') return 'pull'
  if (pattern === 'compression') return 'compression'
  if (pattern === 'core') return 'core'
  if (pattern === 'mobility') return 'flexibility'
  
  // Fallback based on category
  if (exercise.category === 'warmup') return 'warmup'
  if (exercise.category === 'cooldown') return 'cooldown'
  
  return 'accessory'
}

/**
 * Check if exercise is a hold/isometric
 */
export function isIsometricExercise(exercise: Exercise): boolean {
  if (exercise.isIsometric !== undefined) return exercise.isIsometric
  // Infer from repsOrTime format
  return exercise.defaultRepsOrTime.includes('s') || exercise.defaultRepsOrTime.includes('sec')
}

/**
 * Multi-criteria exercise filter for Program Builder
 */
export interface ExerciseFilterCriteria {
  categories?: ExerciseCategory[]
  movementPatterns?: MovementPattern[]
  movementCategories?: MovementCategory[]
  difficultyLevels?: DifficultyLevel[]
  equipment?: EquipmentType[]
  supportsBands?: boolean
  supportsRPE?: boolean
  isIsometric?: boolean
  transfersTo?: string[]
  maxFatigueCost?: number
  maxNeuralDemand?: number
}

/**
 * Filter exercises using multiple criteria
 */
export function filterExercises(criteria: ExerciseFilterCriteria): Exercise[] {
  let exercises = getAllExercises()
  
  if (criteria.categories?.length) {
    exercises = exercises.filter(e => criteria.categories!.includes(e.category))
  }
  
  if (criteria.movementPatterns?.length) {
    exercises = exercises.filter(e => criteria.movementPatterns!.includes(e.movementPattern))
  }
  
  if (criteria.movementCategories?.length) {
    exercises = exercises.filter(e => {
      const cat = inferMovementCategory(e)
      return criteria.movementCategories!.includes(cat)
    })
  }
  
  if (criteria.difficultyLevels?.length) {
    exercises = exercises.filter(e => {
      const level = inferDifficultyLevel(e)
      return criteria.difficultyLevels!.includes(level)
    })
  }
  
  if (criteria.equipment?.length) {
    exercises = exercises.filter(e => hasRequiredEquipment(e, criteria.equipment!))
  }
  
  if (criteria.supportsBands !== undefined) {
    exercises = exercises.filter(e => 
      criteria.supportsBands ? e.supportsBandAssistance === true : true
    )
  }
  
  if (criteria.supportsRPE !== undefined) {
    exercises = exercises.filter(e => 
      criteria.supportsRPE ? e.supportsRPE !== false : true
    )
  }
  
  if (criteria.isIsometric !== undefined) {
    exercises = exercises.filter(e => isIsometricExercise(e) === criteria.isIsometric)
  }
  
  if (criteria.transfersTo?.length) {
    exercises = exercises.filter(e => 
      e.transferTo.some(t => criteria.transfersTo!.includes(t))
    )
  }
  
  if (criteria.maxFatigueCost !== undefined) {
    exercises = exercises.filter(e => e.fatigueCost <= criteria.maxFatigueCost!)
  }
  
  if (criteria.maxNeuralDemand !== undefined) {
    exercises = exercises.filter(e => e.neuralDemand <= criteria.maxNeuralDemand!)
  }
  
  return exercises
}

/**
 * Get a balanced exercise selection across movement categories
 */
export function getBalancedExerciseSelection(
  targetCount: number,
  criteria: ExerciseFilterCriteria = {}
): Exercise[] {
  const filtered = filterExercises(criteria)
  
  // Group by movement category
  const byCategory: Record<MovementCategory, Exercise[]> = {
    push: [],
    pull: [],
    compression: [],
    core: [],
    flexibility: [],
    accessory: [],
    warmup: [],
    cooldown: [],
  }
  
  filtered.forEach(e => {
    const cat = inferMovementCategory(e)
    byCategory[cat].push(e)
  })
  
  // Select balanced mix (prioritize push/pull/compression)
  const result: Exercise[] = []
  const priorities: MovementCategory[] = ['push', 'pull', 'compression', 'core', 'accessory', 'flexibility']
  
  let idx = 0
  while (result.length < targetCount && idx < priorities.length * targetCount) {
    const cat = priorities[idx % priorities.length]
    const available = byCategory[cat].filter(e => !result.includes(e))
    if (available.length > 0) {
      result.push(available[0])
    }
    idx++
  }
  
  return result
}
