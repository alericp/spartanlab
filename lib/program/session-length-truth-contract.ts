/** Drift in minutes beyond which we surface a `labelDriftWarning`. */
const LABEL_DRIFT_TOLERANCE_MIN = 5

/** Default per-row work-time fallback when no explicit setSeconds available. */
const DEFAULT_SET_SECONDS = 45 // typical strength set
/** Default rest fallback when restSeconds missing. */
const DEFAULT_REST_SECONDS = 90

/** Material-distinctness duration bucket — mirrors compression-engine.ts. */
const DURATION_DISTINCTNESS_THRESHOLD_MIN = 5

