// Adaptive Exercise Pool
// Curated high-value exercise library for calisthenics athletes
// Each exercise includes transfer ratings, fatigue cost, and equipment requirements

export type EquipmentType = 'pull_bar' | 'dip_bars' | 'rings' | 'parallettes' | 'bands' | 'floor' | 'wall'
export type MovementPattern = 'horizontal_push' | 'vertical_push' | 'horizontal_pull' | 'vertical_pull' | 'core' | 'compression' | 'transition' | 'skill' | 'mobility'
export type ExerciseCategory = 'skill' | 'strength' | 'accessory' | 'core' | 'warmup' | 'cooldown' | 'flexibility'

// Extended category for movement classification (push/pull/compression)
export type MovementCategory = 'push' | 'pull' | 'compression' | 'core' | 'flexibility' | 'accessory' | 'warmup' | 'cooldown'

// Difficulty levels for progression
export type DifficultyLevel = 'beginner' | 'intermediate' | 'advanced' | 'elite'

export interface MethodCompatibility {
  straightSets?: boolean
  superset?: boolean
  density?: boolean
  dropSet?: boolean
  ladder?: boolean
  emom?: boolean
  clusterSet?: boolean
  restPause?: boolean
}

export type FailureRisk = 'low' | 'moderate' | 'high'

export interface Exercise {
  id: string
  name: string
  category: ExerciseCategory
  movementPattern: MovementPattern
  primaryMuscles: string[]
  equipment: EquipmentType[]
  neuralDemand: 1 | 2 | 3 | 4 | 5 // 5 = highest skill/neural requirement
  fatigueCost: 1 | 2 | 3 | 4 | 5 // 5 = most fatiguing
  transferTo: string[] // skill IDs this helps
  defaultSets: number
  defaultRepsOrTime: string
  notes?: string
  alternatives?: string[] // exercise IDs that can substitute
  methodCompatibility?: MethodCompatibility // training methods this exercise works with
  failureRisk?: FailureRisk // Risk level when training near failure
  dropSetProgression?: string[] // Easier variations for mechanical drop sets
  supportsBandAssistance?: boolean // Whether this exercise can use band assistance
  // New fields for enhanced exercise library
  difficultyLevel?: DifficultyLevel // beginner/intermediate/advanced/elite
  movementCategory?: MovementCategory // push/pull/compression classification
  supportsRPE?: boolean // Whether RPE tracking is meaningful for this exercise
  isIsometric?: boolean // Whether this is a hold/static exercise
  targetTimeUnderTension?: string // For isometric exercises
  // Progression system fields
  progressionUp?: string // ID of the harder progression
  progressionDown?: string // ID of the easier regression
  substitutionOptions?: string[] // Exercise IDs that can substitute this movement
  progressionLadder?: string // ID of the progression ladder this belongs to
}

