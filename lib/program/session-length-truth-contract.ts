// =============================================================================
// [PHASE-R] SESSION-LENGTH TRUTH CONTRACT
// =============================================================================
// Authoritative deterministic READER over the variants already produced by
// `generateSessionVariants` in lib/session-compression-engine.ts. Phase R is
// NOT a second program builder. It does NOT pick exercises, mutate sets, alter
// RPE, change rest, or reorder anything. It reads the canonical full session
// + the existing variants[] array (full / 45 / 30, when launchable and
// materially distinct), computes a per-variant TRUTH RECEIPT (actual minutes
// estimated from the variant body, exercises kept vs deferred, set deltas,
// rest deltas, priority preservation, liveness, runtime safety), and stamps
// it on `session.sessionLengthTruth` for the card and Phase Q to consume.
//
// WHY PHASE R EXISTS
// Phase Q honestly reported session-length as "PARTIALLY CAUSAL — suppressed
// at structural level" because its `evaluateSessionLength()` only compared
// `session.estimatedMinutes` against `profileSnapshot.timeAvailability`. It
// never inspected `session.variants[]`. The variants ARE structurally real:
//   - generated by `generateSessionVariants` from compressed bodies,
//   - gated by `isVariantLaunchable` so empty/hollow variants are dropped,
//   - gated by `areVariantsMateriallyDistinct` so cosmetic-only shorts are
//     dropped (same membership / same ordered identity / same total sets /
//     near-identical duration → rejected),
//   - regenerated POST-grouping-materialization so 45/30 inherit atomic-unit
//     awareness (see adaptive-program-builder.ts L14705).
// What was missing was an honest TRACE that Phase Q (and the card) could read
// to verify the variants are real and to surface what each mode actually is.
//
// ARCHITECTURAL PRINCIPLES (from the Phase R brief)
//   1. Full / 45 / 30 are a deterministic resolver over canonical truth, not
//      three programs and not a CSS filter. We READ the resolver output, we
//      do not re-run it.
//   2. Canonical full session remains intact. Phase R never overwrites
//      `session.exercises`, never overwrites `variant.selection`, never
//      mutates sets/reps/RPE/rest.
//   3. We STAMP `session.sessionLengthTruth`. Optional, JSON-safe,
//      round-trips through every existing `...session` spread (program-state,
//      normalize, save/load) like `qualityAudit` and `doctrineUtilizationTrace`.
//   4. No new dependencies. No Prisma changes. No live workout reducer
//      changes. No new builder.
//
// HONESTY GUARDS
//   - If `variants[]` is missing or has only Full → state is ACKNOWLEDGED_ONLY
//     with `verdict: 'NO_LAUNCHABLE_SHORTS'`.
//   - If a short variant exists but compresses zero exercises and has same
//     total sets (i.e. cosmetic) → that variant is dropped from the truth
//     (matching the upstream distinctness gate behavior).
//   - If actual computed minutes drift more than the
//     `LABEL_DRIFT_TOLERANCE_MIN` from the variant's labeled duration → we
//     stamp `labelDriftWarning: true` so the card renders an honest
//     "Estimated 36 min because primary skill + safe rest minimums prevent
//      further compression" instead of pretending it lands at exactly 30.
//   - We never overwrite `variant.duration`. We expose `actualEstimatedMinutes`
//     alongside it.
//
// CONSUMERS
//   - lib/server/authoritative-program-generation.ts runs this BEFORE Phase Q
//     so Phase Q's `evaluateSessionLength()` can read `session.sessionLengthTruth`
//     and credit ELIGIBLE_AND_APPLIED when structural compression actually
//     produced distinct shorts.
//   - lib/program/performance-feedback-integration.ts (client boot overlay)
//     runs this in the same position so client-side regenerations stamp
//     identical truth.
//   - components/programs/AdaptiveSessionCard.tsx renders one compact line
//     ("3 modes structurally distinct · short variants drop 2 accessories
//      · primary skill anchor preserved") under the variant button row.
//
// LIVE WORKOUT HANDOFF
//   The card already routes via `selectedSessionContract.selectedLaunchUrl`,
//   `buildSessionFingerprint`, and `stampLaunchFingerprint`. Phase R's job
//   is NOT to rewire that corridor — it's to PROVE the corridor is honest by
//   producing a `liveWorkoutHandoff: { mechanism, payloadCompatibility }`
//   field the master-truth blueprint can audit.
// =============================================================================

