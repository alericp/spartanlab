/**
 * Run the Phase R Session-Length Truth Contract. Pure reader. Stamps
 * `session.sessionLengthTruth` on every session and `program.sessionLengthTruth`
 * on the program. Never mutates exercises, sets, RPE, rest, methods, ordering.
 *
 * Safe to call multiple times — overwrites the stamp deterministically.
 */
export function runSessionLengthTruthContract(
  programInput: AdaptiveProgram,
): {
  program: AdaptiveProgram
  audit: {
    attempted: true
    stampedSessions: number
    structurallyReal: number
    labelParity: number
    noLaunchableShorts: number
    legacyNoVariants: number
    programVerdict: ProgramSessionLengthTruthStamp['programVerdict']
    summary: string
  }
} {
  const program = programInput as unknown as ProgramLike
  const sessionsIn: SessionLike[] = Array.isArray(program.sessions) ? program.sessions : []

  const newSessions: AdaptiveSession[] = []
  const perSessionStamps: SessionLengthTruthStamp[] = []
  for (const s of sessionsIn) {
    const stamp = buildSessionStamp(s)
    perSessionStamps.push(stamp)
    newSessions.push({
      ...(s as unknown as AdaptiveSession),
      sessionLengthTruth: stamp,
    } as AdaptiveSession)
  }

  const programStamp = buildProgramStamp(perSessionStamps)

  const out: AdaptiveProgram = {
    ...(programInput as AdaptiveProgram),
    sessions: newSessions,
    sessionLengthTruth: programStamp,
  } as AdaptiveProgram

  return {
    program: out,
    audit: {
      attempted: true,
      stampedSessions: perSessionStamps.length,
      structurallyReal: programStamp.sessionCounts.structurallyReal,
      labelParity: programStamp.sessionCounts.labelParity,
      noLaunchableShorts: programStamp.sessionCounts.noLaunchableShorts,
      legacyNoVariants: programStamp.sessionCounts.legacyNoVariants,
      programVerdict: programStamp.programVerdict,
      summary: programStamp.summary,
    },
  }
}
